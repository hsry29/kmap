// ─────────────────────────────────────────────────────────────────────────────
// 장소 이름 → 카카오 좌표/주소 자동 변환 스크립트
//
// 사용법:
//   1) scripts/place-names.txt 에 추가할 장소를 적습니다.
//      ## 테마이름                  ← 그룹 헤더
//      - 장소 검색어 지역힌트       ← 카카오에 그대로 검색됨
//   2) .env 에 KAKAO_REST_API_KEY 가 있어야 합니다.
//   3) npm run resolve:names
//
// 결과:
//   src/data/places.resolved.js 가 생성됩니다.
//   검토 후 좋은 항목만 src/data/places.js 의 RAW_THEME_PLACES 에 옮기세요.
//
// 콘솔 출력:
//   각 검색의 매칭 결과를 출력합니다. 후보가 여럿이면 상위 3개를 함께 보여줍니다.
//   결과가 어색하면 place-names.txt 의 키워드를 더 구체적으로 고쳐 다시 실행하세요.
// ─────────────────────────────────────────────────────────────────────────────

import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const ROOT = path.resolve(__dirname, '..')

// ─── .env 파싱 (의존성 없이) ─────────────────────────────────────────────────
function loadEnv() {
  const envPath = path.join(ROOT, '.env')
  if (!fs.existsSync(envPath)) return
  const text = fs.readFileSync(envPath, 'utf8')
  for (const line of text.split(/\r?\n/)) {
    const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*?)\s*$/)
    if (!m) continue
    const [, key, raw] = m
    if (process.env[key]) continue
    process.env[key] = raw.replace(/^['"]|['"]$/g, '')
  }
}
loadEnv()

const REST_KEY = process.env.KAKAO_REST_API_KEY
if (!REST_KEY) {
  console.error('❌ KAKAO_REST_API_KEY 환경변수가 필요합니다.')
  console.error('   Kakao Developers > 내 앱 > 앱 키 > "REST API 키" 를 .env 에 추가하세요.')
  process.exit(1)
}

if (typeof fetch !== 'function') {
  console.error('❌ Node 18 이상에서 실행해야 합니다 (built-in fetch 필요).')
  process.exit(1)
}

const INPUT_PATH = path.join(__dirname, 'place-names.txt')
const OUTPUT_PATH = path.join(ROOT, 'src', 'data', 'places.resolved.js')

if (!fs.existsSync(INPUT_PATH)) {
  console.error(`❌ 입력 파일이 없습니다: ${path.relative(ROOT, INPUT_PATH)}`)
  process.exit(1)
}

const SLEEP_MS = 120 // 호출 간격
const TOP_N = 5 // 카카오에서 받아올 후보 수

// ─── 유틸 ───────────────────────────────────────────────────────────────────
const sleep = (ms) => new Promise((resolve) => setTimeout(resolve, ms))

function toSlug(input) {
  return (
    String(input)
      .toLowerCase()
      .normalize('NFKD')
      .replace(/[^a-z0-9]+/g, '-')
      .replace(/^-+|-+$/g, '')
      .slice(0, 30) || 'item'
  )
}

function parseInput(text) {
  const groups = []
  let current = null
  for (const raw of text.split(/\r?\n/)) {
    const line = raw.replace(/\s+$/, '')
    const stripped = line.trim()
    if (!stripped) continue
    if (stripped.startsWith('//') || stripped.startsWith('#') && !stripped.startsWith('##')) {
      continue
    }
    const header = stripped.match(/^##\s*(.+)$/)
    if (header) {
      current = { theme: header[1].trim(), items: [] }
      groups.push(current)
      continue
    }
    const item = stripped.match(/^-\s*(.+)$/)
    if (item && current) {
      current.items.push(item[1].trim())
    }
  }
  return groups
}

async function searchKeyword(query) {
  const url = new URL('https://dapi.kakao.com/v2/local/search/keyword.json')
  url.searchParams.set('query', query)
  url.searchParams.set('size', String(TOP_N))
  url.searchParams.set('sort', 'accuracy')

  const res = await fetch(url, {
    headers: { Authorization: `KakaoAK ${REST_KEY}` },
  })
  if (!res.ok) {
    throw new Error(`Kakao API ${res.status} ${res.statusText}: ${await res.text()}`)
  }
  const data = await res.json()
  return data.documents || []
}

function toResolvedItem(theme, query, doc) {
  return {
    id: `resolved-${toSlug(theme)}-${doc.id}`,
    region: '',
    enName: doc.place_name,
    koAddress: doc.road_address_name || doc.address_name || '',
    lat: Number(doc.y),
    lng: Number(doc.x),
    phone: doc.phone || '',
    categoryName: doc.category_name || '',
    placeUrl: doc.place_url || '',
    _searchQuery: query,
  }
}

function renderOutput(grouped, stats) {
  const banner = [
    '// ─────────────────────────────────────────────────────────────────────────────',
    '// AUTO-GENERATED by scripts/resolve-place-names.mjs',
    `// Generated: ${new Date().toISOString()}`,
    `// Source:    scripts/place-names.txt`,
    `// Stats:     matched=${stats.matched}, missed=${stats.missed}, failed=${stats.failed}`,
    '//',
    '// 검토 후 좋은 항목만 src/data/places.js 의 RAW_THEME_PLACES 로 옮기세요.',
    '// (_searchQuery 필드는 디버그용입니다. 옮길 때 빼셔도 됩니다.)',
    '// ─────────────────────────────────────────────────────────────────────────────',
    '',
  ].join('\n')

  return `${banner}export const RESOLVED_PLACES = ${JSON.stringify(grouped, null, 2)}\n`
}

// ─── 메인 ───────────────────────────────────────────────────────────────────
async function main() {
  const inputText = fs.readFileSync(INPUT_PATH, 'utf8')
  const groups = parseInput(inputText)
  const totalItems = groups.reduce((sum, g) => sum + g.items.length, 0)

  if (totalItems === 0) {
    console.error('❌ 입력 파일에서 처리할 항목을 찾지 못했습니다.')
    console.error('   "## 테마" 헤더 아래에 "- 검색어" 줄을 추가해주세요.')
    process.exit(1)
  }

  console.log(`▶ 카카오 이름→좌표 변환 시작 (총 ${totalItems}건)\n`)

  const grouped = {}
  const stats = { matched: 0, missed: 0, failed: 0 }

  for (const group of groups) {
    if (group.items.length === 0) continue
    console.log(`■ ${group.theme} (${group.items.length}건)`)
    grouped[group.theme] = []

    for (const query of group.items) {
      try {
        const docs = await searchKeyword(query)
        if (docs.length === 0) {
          console.log(`  ⚠ "${query}" → 결과 없음`)
          stats.missed += 1
          continue
        }
        const top = docs[0]
        const item = toResolvedItem(group.theme, query, top)
        grouped[group.theme].push(item)
        stats.matched += 1

        console.log(`  ✓ "${query}"`)
        console.log(`     → ${top.place_name}`)
        console.log(`       ${top.road_address_name || top.address_name || '(주소 없음)'}`)
        if (top.category_name) console.log(`       [${top.category_name}]`)
        if (docs.length > 1) {
          const others = docs.slice(1, 4).map((d) => d.place_name).join(' / ')
          console.log(`     ※ 다른 후보: ${others}`)
        }
      } catch (err) {
        console.warn(`  ✗ "${query}" → 실패: ${err.message}`)
        stats.failed += 1
      }
      await sleep(SLEEP_MS)
    }
    console.log('')
  }

  fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true })
  fs.writeFileSync(OUTPUT_PATH, renderOutput(grouped, stats), 'utf8')

  console.log('─'.repeat(60))
  console.log(`✔ 매칭: ${stats.matched}건 / 결과없음: ${stats.missed}건 / 실패: ${stats.failed}건`)
  console.log(`  → ${path.relative(ROOT, OUTPUT_PATH)}`)
  console.log('\n다음 단계:')
  console.log('  1) places.resolved.js 를 열어 매칭 결과를 검토')
  console.log('  2) 정확한 항목만 src/data/places.js 의 RAW_THEME_PLACES 로 복사')
  console.log('  3) 어색한 매칭은 place-names.txt 키워드를 더 구체적으로 고쳐 재실행')
}

main().catch((err) => {
  console.error('\n❌ 스크립트 실패:', err)
  process.exit(1)
})
